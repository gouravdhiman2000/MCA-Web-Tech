let str= "Hello World Learning Strings";
// split the string where there is spaces in the string
let arr=str.split(" ");
console.log(arr);

// to join array into string
let newStr=arr.join("-");
console.log(newStr);

// reverse the string
let revStr= arr.reverse("");
console.log(revStr);

// to find index of the word
let index = str.indexOf("World");
console.log(index);