// 1. using querySelector : will give only on element
//  using querySelectorAll : gives collection of result
let movies = document.querySelectorAll('.movie');
console.log(movies);

// 2. Selecting id using querySelector

let gadar = document.querySelector('#gadar');
console.log(gadar);

// 3. Tagname se lekar aaana hai
let div= document.querySelectorAll('div');
console.log(div);

// 4. Pick the li item that has the class name - ironman
let ironman = document.querySelector('li.ironman');
console.log(ironman);


